"use strict";

////////////////////////////////////////////////////////////////////////////////
// vine動画再生
////////////////////////////////////////////////////////////////////////////////
Contents.vine = function( cp )
{
	var p = $( '#' + cp.id );
	var cont = p.find( 'div.contents' );

	cp.SetIcon( null );

	////////////////////////////////////////////////////////////
	// 開始処理
	////////////////////////////////////////////////////////////
	this.start = function() {
		cont.activity( { color: '#ffffff' } );

		cont.addClass( 'vine' )
			.html( OutputTPL( 'vine', { url: cp.param['url'] + '/card' } ) );

		cp.SetTitle( 'vine - ' + cp.param['screen_name'] + ' ' + cp.param['url'], false );
		cont.activity( false );
	};

	////////////////////////////////////////////////////////////
	// 終了処理
	////////////////////////////////////////////////////////////
	this.stop = function() {
	};
}
